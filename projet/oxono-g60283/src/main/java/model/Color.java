package model;

/**
 * Enum representing the colors available in the game.
 * <p>
 * This enum provides two possible values:
 * - PINK
 * - BLACK
 * <p>
 * Each color can be used to represent a player or an element in the game.
 *
 * @author Salim
 */
public enum Color {
    /**
     * Represents the pink color.
     */
    PINK,

    /**
     * Represents the black color.
     */
    BLACK
}
