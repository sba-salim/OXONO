package model;

/**
 * Enum representing the symbols available in the game.
 * <p>
 * This enum provides two possible values:
 * - X
 * - O
 * <p>
 * Each symbol can be used to represent a player or a game element on the board.
 *
 * @author Salim
 */
public enum Symbol {
    /**
     * Represents the 'X' symbol.
     */
    X,

    /**
     * Represents the 'O' symbol.
     */
    O
}
